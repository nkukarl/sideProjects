import fractions

calc = {'+' : lambda x, y : x + y, '-' : lambda x, y : x - y, '*' : lambda x, y : x * y, '/' : lambda x, y : float(x) / y}
ops = itertools.permutations(['+', '-', '*', '/'] * 3, 3)
ops = [tuple(sorted(op)) for op in ops]
ops = set(ops)

def evalRPN_opt(tokens):
	flag = 1
	stack = []
	for t in tokens:
		if isDigit(t):
			stack.append(fractions.Fraction(t))
		else:
			try:
				stack.append(calc[t](stack.pop(-2), stack.pop(-1)))
			except:
				flag = 0
				break
	if flag and len(stack) == 1:
		return stack[0]
	return 0

def isDigit(token):
	try:
		token = int(token)
		return True
	except:
		return False

def game(card):
	solution = []
	for op in ops:
		op1, op2, op3 = op
		space = card + [op1, op2, op3]
		tokens = list(set(itertools.permutations(space)))
		for t in tokens:
			if evalRPN_opt(t) == 24:
				solution += [t]
	return set(solution)

